package co.com.choucair.certification.utest.models;

public class UtestChooseDeviceData {

    private String computer;
    private String version;
    private String language;
    private String movileDevice;
    private String model;
    private String operatingSystem;


    public String getComputer() {
        return computer;
    }

    public void setComputer(String computer) {
        this.computer = computer;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getMovileDevice() {
        return movileDevice;
    }

    public void setMovileDevice(String movileDevice) {
        this.movileDevice = movileDevice;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOperatingSystem() {
        return operatingSystem;
    }

    public void setOperatingSystem(String operatingSystem) {
        this.operatingSystem = operatingSystem;
    }

}
